camisas = int(input("Digete a quantidade de camisas no seu guarda roupa: "))
calcas = int(input("Digite a quantidade de calças no seu guarda roupa:"))
sapatos = int(input(" Digite a quantidade de sapatos no seu guarda roupa:"))

resultado = camisas * calcas * sapatos

print(f"Você podera se vestir de {resultado} maneiras diferentes")
